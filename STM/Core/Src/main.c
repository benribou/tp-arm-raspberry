bool dht_read_data(uint8_t *dht_value){
    uint8_t RHI = 0, RHD = 0, TCI = 0, TCD = 0, SUM = 0;
    if(DHT11_Start()){
        RHI = DHT11_Read();
        RHD = DHT11_Read();
        TCI = DHT11_Read();
        TCD = DHT11_Read();
        SUM = DHT11_Read();
        if (RHI + RHD + TCI + TCD == SUM){
            dht_value[0] = RHI;
            dht_value[1] = RHD;
            dht_value[2] = TCI;
            dht_value[3] = TCD;
            return true;
        }
    }
    return false;
}

